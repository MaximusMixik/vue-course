// Пример использования в Vue 3 Options API
export const carFilters = {
	data() {
		return {
			cars: carsData,
			filteredCars: carsData,
			filters: {
				vehicleType: 'legkovi', // по умолчанию легковые
				bodyType: '',
				brand: '',
				model: '',
				yearFrom: '',
				yearTo: ''
			},
			availableModels: [] // для динамического заполнения моделей
		}
	},

	watch: {
		// При изменении марки - обновляем список моделей
		'filters.brand'(newBrand) {
			this.availableModels = newBrand && modelsByBrand[newBrand]
				? [{ value: '', label: 'Оберіть' }, ...modelsByBrand[newBrand]]
				: [{ value: '', label: 'Оберіть' }];
			this.filters.model = ''; // сбрасываем выбранную модель
		}
	},

	methods: {
		applyFilters() {
			this.filteredCars = this.cars.filter(car => {
				return (
					(!this.filters.vehicleType || car.vehicleType === this.filters.vehicleType) &&
					(!this.filters.bodyType || car.bodyType === this.filters.bodyType) &&
					(!this.filters.brand || car.brand === this.filters.brand) &&
					(!this.filters.model || car.model === this.filters.model) &&
					(!this.filters.yearFrom || car.year >= parseInt(this.filters.yearFrom)) &&
					(!this.filters.yearTo || car.year <= parseInt(this.filters.yearTo))
				);
			});
		},

		resetFilters() {
			this.filters = {
				vehicleType: 'legkovi',
				bodyType: '',
				brand: '',
				model: '',
				yearFrom: '',
				yearTo: ''
			};
			this.availableModels = [{ value: '', label: 'Оберіть' }];
			this.filteredCars = this.cars;
		},

		// Метод для получения моделей по марке
		getModelsByBrand(brandValue) {
			return brandValue && modelsByBrand[brandValue]
				? [{ value: '', label: 'Оберіть' }, ...modelsByBrand[brandValue]]
				: [{ value: '', label: 'Оберіть' }];
		}
	}
};